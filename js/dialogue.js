  var lastLine = null; // 直前に返したセリフ(2連続回避用)

  function applySuffix(text, suffix) {
    if (!suffix) { return text; }
    // 全文一律: 各文末の 。!?♪… の連なりの直前に suffix を挿入。
    // 例:「だよ。がんばって!」→「だよ<suffix>。がんばって<suffix>!」
    // ただし文中の間を表す「…」だけの連なり(「えっと…そのね。」)には挿入しない。
    // 文章の末尾なら「…」だけでも挿入する(「どうかな…」→「どうかな<suffix>…」)。
    var endIdx = text.replace(/\s+$/, '').length;
    var inserted = false;
    var s = text.replace(/([^。!?♪…\s])([。!?♪…]+)/g, function (m, ch, marks, offset) {
      var atEnd = (offset + m.length) >= endIdx;
      if (/[。!?♪]/.test(marks) || atEnd) {
        inserted = true;
        return ch + suffix + marks;
      }
      return m;
    });
    // 終端記号がひとつもなければ末尾に付ける
    if (!inserted) { s = s + suffix; }
    return s;
  }

  function format(text, state) {
    if (text == null) { return ''; }
    var s = String(text);
    var ch = (state && state.character) ? state.character : {};
    var user = ch.callName != null ? ch.callName : '';
    var me = ch.firstPerson != null ? ch.firstPerson : '';
    s = s.replace(/\{user\}/g, user)
         .replace(/\{me\}/g, me);
    // {task} は extra 経由で get() 側が差し込むが、format 単体でも安全に空置換しておく
    s = s.replace(/\{task\}/g, (state && state.__task != null) ? state.__task : '');
    s = applySuffix(s, ch.suffix);
    return s;
  }

  function pick(arr) {
    if (!arr || !arr.length) { return null; }
    if (arr.length === 1) { return arr[0]; }
    var candidate;
    var guard = 0;
    do {
      candidate = arr[Math.floor(Math.random() * arr.length)];
      guard++;
    } while (candidate === lastLine && guard < 8);
    return candidate;
  }

  /** personality がプリセットならそれ、カスタムなら basePersonality (それも無ければ tsundere) */
  function resolvePersonality(state) {
    var ch = (state && state.character) || {};
    if (DIALOGUE[ch.personality]) { return ch.personality; }
    if (DIALOGUE[ch.basePersonality]) { return ch.basePersonality; }
    return 'tsundere';
  }

  /** カスタムセリフ集のプール取り出し。配列 (全 tier 共通) と {low,mid,high} の両形式に対応 */
  function customPool(entry, tier) {
    if (!entry) { return null; }
    if (Array.isArray(entry)) { return entry.length ? entry : null; }
    var pool = entry[tier] || entry.low || entry.mid || entry.high;
    return (Array.isArray(pool) && pool.length) ? pool : null;
  }

  function get(situation, state, extra) {
    extra = extra || {};
    var tier = (window.GameData && window.GameData.tierFor)
      ? window.GameData.tierFor(state ? state.affection : 0)
      : 'low';

    // カスタムセリフ集 (インポートされたフルカスタム性格) を最優先
    var custom = state && state.character && state.character.customDialogue;
    var pool = (state && state.character && state.character.personality === 'custom' && custom)
      ? customPool(custom[situation], tier)
      : null;

    if (!pool) {
      var pdata = DIALOGUE[resolvePersonality(state)];
      var sitData = pdata[situation];
      if (!sitData) {
        // 未知の situation は idle で代替
        sitData = pata_idle_fallback(pdata);
      }
      pool = sitData[tier] || sitData.low || sitData.mid || sitData.high;
    }
    var raw = pick(pool);
    if (raw == null) { return ''; }
    lastLine = raw;
    // {task} を format に渡すため state へ一時的に格納
    var taskVal = (extra.task != null) ? extra.task : '';
    var proxyState = state || {};
    var prevTask = proxyState.__task;
    proxyState.__task = taskVal;
    var out = format(raw, proxyState);
    proxyState.__task = prevTask; // 元に戻す
    return out;
  }

  function pata_idle_fallback(pdata) {
    return pdata.idle || { low: [''], mid: [''], high: [''] };
  }


